		<h3><b>Misión</b></h3>
		<p>La fundación ENTREAMIGOS es una institución que pretende hacer un aporte a la obra social a través del área de la protección de niños, previniendo el maltrato infantil.</p>
		<p>Extenderemos una red por toda Colombia, construida con personas que rechacen este flagelo  y que denuncien el maltrato sin correr ningún riesgo.</p>
		<p>También creamos un área en el campo de la cultura con el fin de tener contacto permanente con nuestros asociados, nuestro producto son libros, cuentos para niños y adultos, historias que permitan hacer un buen ejercicio de lectura y un aporte a la cultura, creada en la fundación.</p>
	</div>
	<div class="well">
		<h3>Visión</h3>
		<p>Para finales del año 2017, seremos una institución reconocida en todas las ciudades y pueblos de Colombia por promover la cultura y el rechazo al maltrato infantil y esperamos llevar este programa  como mínimo a dos países de Suramérica y que podamos ser los pioneros en una gran propuesta  de lectura educativa.</p>
